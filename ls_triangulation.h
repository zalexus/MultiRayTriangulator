#pragma once // Гарантирует, что этот заголовочный файл будет включен компилятором только один раз за сессию сборки
#include <vector> // Подключаем стандартный контейнер динамических массивов STL для хранения данных кадров
#include <cmath>  // Подключаем стандартную математическую библиотеку для операций взятия модуля (abs) и корня (sqrt)

// Базовая структура для представления трехмерного вектора или точки в мировых координатах сцены Nuke
struct Vector3D {
    double x, y, z; // Компоненты трехмерного пространства высокой точности (вещественные числа с плавающей точкой)
};

// Структура, объединяющая геометрические параметры съемочного луча для конкретного кадра таймлайна
struct FrameRayData {
    Vector3D cameraOrigin; // 3D-точка текущего положения оптического центра съемочной камеры в мировом пространстве
    Vector3D worldDir;     // Нормированный 3D-вектор направления луча, исходящего из камеры через точку трекера на сенсоре
};

// Главная математическая функция аналитического расчета МНК-пересечения произвольного количества трехмерных лучей
inline bool computeMultiRayIntersection(const std::vector<FrameRayData>& gatheredFrames, double* outPos) {
    outPos[0] = 0.0; // Инициализируем результирующую координату X нулевым значением на случай ошибки расчета
    outPos[1] = 0.0; // Инициализируем результирующую координату Y нулевым значением на случай ошибки расчета
    outPos[2] = 0.0; // Инициализируем результирующую координату Z нулевым значением на случай ошибки расчета

    if (gatheredFrames.empty()) return false; // Если массив входных лучей пуст, математический расчет невозможен, выходим

    double M[3][3] = {{0.0}}; // Объявляем и обнуляем системную матрицу 3x3 для метода наименьших квадратов (МНК)
    double b[3] = {0.0};      // Объявляем и обнуляем результирующий вектор свободных членов (правая часть уравнения)
    int validRaysCount = 0;   // Переменная для подсчета количества лучей, успешно прошедших валидацию по длине вектора

    // Начинаем итерационный цикл по всем собранным кадрам и их геометрическим лучам
    for (const auto& frame : gatheredFrames) {
        double dx = frame.worldDir.x; // Вытаскиваем координату X направляющего вектора луча для текущего кадра
        double dy = frame.worldDir.y; // Вытаскиваем координату Y направляющего вектора луча для текущего кадра
        double dz = frame.worldDir.z; // Вытаскиваем координату Z направляющего вектора луча для текущего кадра

        double P[3][3]; // Объявляем локальную матрицу проекции луча P = I - d * d^T (где I — единичная матрица)
        P[0][0] = 1.0 - dx * dx; P[0][1] = -dx * dy;     P[0][2] = -dx * dz; // Расчет первой строки матрицы проекции луча
        P[1][0] = -dy * dx;     P[1][1] = 1.0 - dy * dy; P[1][2] = -dy * dz; // Расчет второй строки матрицы проекции луча
        P[2][0] = -dz * dx;     P[2][1] = -dz * dy;     P[2][2] = 1.0 - dz * dz; // Расчет третьей строки матрицы проекции луча

        // Вложенный цикл для аккуратного суммирования локальной матрицы проекции в общую системную матрицу M
        for (int i = 0; i < 3; ++i) { // Перебор строк от 0 до 2
            for (int j = 0; j < 3; ++j) { // Перебор столбцов от 0 до 2
                M[i][j] += P[i][j]; // Накапливаем сумму проекций всех лучей в текущей ячейке системной матрицы M
            }
            // Формируем i-ю компоненту вектора b путем перемножения строки матрицы проекции на координаты центра камеры
            b[i] += P[i][0] * frame.cameraOrigin.x +
                    P[i][1] * frame.cameraOrigin.y +
                    P[i][2] * frame.cameraOrigin.z;
        }
        validRaysCount++; // Увеличиваем счетчик обработанных и геометрически валидных лучей сцены
    }

    if (validRaysCount < 1) return false; // Если не удалось собрать ни одного луча, МНК-пересечение построить нельзя

    // Вычисляем определитель (детерминант) накопленной системной матрицы M 3x3 по классическому правилу Саррюса
    double det = M[0][0] * (M[1][1] * M[2][2] - M[1][2] * M[2][1]) -
                 M[0][1] * (M[1][0] * M[2][2] - M[1][2] * M[2][0]) +
                 M[0][2] * (M[1][0] * M[2][1] - M[1][1] * M[2][0]);

    // Проверяем определитель на близость к нулю. Если он слишком мал, лучи параллельны или компланарны (матрица вырождена)
    if (std::abs(det) <= 1e-7) return false; // Во избежание деления на ноль прерываем расчет и возвращаем флаг неудачи

    double M_inv[3][3]; // Объявляем матрицу 3x3, в которую запишем результат инверсии системной матрицы M
    // Математический расчет обратной матрицы через алгебраические дополнения элементов исходной матрицы M, деленные на определитель
    M_inv[0][0] =  (M[1][1] * M[2][2] - M[1][2] * M[2][1]) / det; // Верхний левый элемент обратной матрицы
    M_inv[0][1] = -(M[0][1] * M[2][2] - M[0][2] * M[2][1]) / det; // Верхний средний элемент обратной матрицы
    M_inv[0][2] =  (M[0][1] * M[1][2] - M[0][2] * M[1][1]) / det; // Верхний правый элемент обратной матрицы

    M_inv[1][0] = -(M[1][0] * M[2][2] - M[1][2] * M[2][0]) / det; // Средний левый элемент обратной матрицы
    M_inv[1][1] =  (M[0][0] * M[2][2] - M[0][2] * M[2][0]) / det; // Центральный элемент обратной матрицы
    M_inv[1][2] = -(M[0][0] * M[1][2] - M[0][2] * M[1][0]) / det; // Средний правый элемент обратной матрицы

    M_inv[2][0] =  (M[1][0] * M[2][1] - M[1][1] * M[2][0]) / det; // Нижний левый элемент обратной матрицы
    M_inv[2][1] = -(M[0][0] * M[2][1] - M[0][1] * M[2][0]) / det; // Нижний средний элемент обратной матрицы
    M_inv[2][2] =  (M[0][0] * M[1][1] - M[0][1] * M[1][0]) / det; // Нижний правый элемент обратной матрицы

    // Находим финальные 3D-координаты искомой точки пересечения путем перемножения инвертированной матрицы M_inv на вектор b
    double resX = M_inv[0][0] * b[0] + M_inv[0][1] * b[1] + M_inv[0][2] * b[2]; // Итоговая минимизированная координата X
    double resY = M_inv[1][0] * b[0] + M_inv[1][1] * b[1] + M_inv[1][2] * b[2]; // Итоговая минимизированная координата Y
    double resZ = M_inv[2][0] * b[0] + M_inv[2][1] * b[1] + M_inv[2][2] * b[2]; // Итоговая минимизированная координата Z

    outPos[0] = resX; // Записываем вычисленное значение X в выходной массив double по нулевому индексу
    outPos[1] = resY; // Записываем вычисленное значение Y в выходной массив double по первому индексу
    outPos[2] = resZ; // Записываем вычисленное значение Z в выходной массив double по второму индексу

    return true; // Возвращаем логическое true, подтверждая успешное завершение триангуляции лучей по МНК
}
